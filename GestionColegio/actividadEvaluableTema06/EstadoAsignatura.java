package actividadEvaluableTema06;

/**
 * Enumeración que representa el estado de una asignatura para un estudiante.
 */
public enum EstadoAsignatura {
	/** La asignatura ha sido aprobada exitosamente. */
	SUPERADO, 
	
	/** La asignatura está en curso. */
	CURSADO, 
	
	/** La asignatura ha sido abandonada sin completarla. */
	ABANDONADO;
}
